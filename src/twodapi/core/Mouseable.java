package twodapi.core;

import java.awt.Dimension;
import java.awt.Point;

/**An interface that allows an implementing class to have mouse input functionality when registered with
 * {@link MouseControls} object.
 *
 * @author bsimpkins
 */
public interface Mouseable
{
    public static final int NO_BUTTON = 0;
    public static final int LEFT_BUTTON = 1;
    public static final int CENTER_BUTTON = 2;
    public static final int RIGHT_BUTTON = 3;

    /**Gets center location of the implementing object
     *
     * @return center location of the implementing object in the component
     */
    public Point getLocation();

    /**Returns the size of the object as it is displayed
     *
     * @return size of the object
     */
    public Dimension getSize();

    /**Fired when mouse button is pressed or released
     *
     * @param mousePoint Location of the mouse in the component
     * @param button button index (see constants)
     * @param pressed <code>true</code> if the button is pressed
     */
    public void mouseButtonUpdate(Point mousePoint, int button, boolean pressed);

    /**Fired when mouse is moved (or dragged).
     *
     * @param mousePoint Location of the mouse in the component
     * @param button button index (if pressed)
     * @param pressed <code>true</code> if the button is pressed
     */
    public void mouseMoveUpdate(Point mousePoint, int button, boolean pressed);

    /**Fired when the mouse cursor enters or exists the object's bounds (which are specified
     * by what is returned in the <code> getLocation()</code> and <code> getSize()</code> methods
     * of this interface
     *
     * @param mousePoint Location of the mouse in the component
     * @param entered <code>true</code> if the mouse cursor entered the object (<code>false</code> if it exited)
     */
    public void mouseBorderUpdate(Point mousePoint, boolean entered);

}
