package twodapi.core;

import java.awt.Graphics;

/**Classes that wish to recieve state updates and paint calls from a {@link Painter} object should
 * implement this class and register with the {@link Painter} by calling <code>Painter</code>'s
 * <code>addToPainterList()</code> method.
 *
 * @author ben
 */
public interface Paintable 
{
    /**Paint update
     *
     * @param g Graphics object to paint to
     */
    public void paintObject(Graphics g);

    /**State update
     *
     * @param interpolation Time since last update (in seconds).
     */
    public void update(float interpolation);
}
