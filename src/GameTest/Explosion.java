package GameTest;

import java.awt.Point;
import twodapi.core.FlipBookSprite;
import twodapi.core.Painter;

/**
 *
 * @author bsimpkins
 */
public class Explosion extends FlipBookSprite
{

    private Painter painter;

    public Explosion(Painter painter)
    {
        super("/images/Explosion/Explosion.gif");

        setPainter(painter);
        setBilinearInterpolation(true);
        setFramesToRenderImage(1);
        setScale(3);
        setVisible(false);
    }


    @Override
    public void updateState(float interpolation)
    {
        if (!isVisible()) return;

        //if last frame is shown, make explosion disappear
        if (this.getFrameIndex() >= 15)
        {
            setVisible(false);
        }

    }


}
